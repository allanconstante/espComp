char callDriver(int drv_id, char func_id, void *parameters)
{
    char i;
    for(i=0; i<qntDrvLoaded; i++)
    {
        if(drv_id == driversLoaded[i]->drv_id)
        {
            return driverLoaded[i]->drv_func[func_id](*parameters);
        }
    }
    return 0;
}